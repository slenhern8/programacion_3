def calcular_itbms(valor):
    """
    Calcualr el ITBMS (7%)

    Admite un valor subtotal para calcular el total (incluyendo el 7% del ITBMS)

    Parameters
    -----------
    valor : float
        subtotal a aplicar ITBMS

    Returns
    -------
    float
        Total con el ITBMS aplicado
    """
    return valor * 0.07